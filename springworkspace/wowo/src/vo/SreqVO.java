package vo;

public class SreqVO {
	private String srnum, srname, sraddress, srtel, srdate;
	private int num;
	
	public String getSrnum() {
		return srnum;
	}
	public void setSrnum(String srnum) {
		this.srnum = srnum;
	}
	public String getSrname() {
		return srname;
	}
	public void setSrname(String srname) {
		this.srname = srname;
	}
	public String getSraddress() {
		return sraddress;
	}
	public void setSraddress(String sraddress) {
		this.sraddress = sraddress;
	}
	public String getSrtel() {
		return srtel;
	}
	public void setSrtel(String srtel) {
		this.srtel = srtel;
	}
	public String getSrdate() {
		return srdate;
	}
	public void setSrdate(String srdate) {
		this.srdate = srdate;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
		
}
